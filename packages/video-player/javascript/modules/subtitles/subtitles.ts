// Word‐level transcript entry
interface WordTiming {
    word: string;
    start: number;
    end: number;
}

// Full transcript JSON entry
interface TranscriptEntry {
    transcript: string;
    confidence: number;
    words: WordTiming[];
    language?: string;
}

